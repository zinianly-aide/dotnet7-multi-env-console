using Microsoft.EntityFrameworkCore;
using MultiModuleApp.Core.Entities;
using MultiModuleApp.Core.Interfaces;
using MultiModuleApp.Infrastructure.Data;

namespace MultiModuleApp.Infrastructure.Services;

public class ProductService : IProductService
{
    private readonly MySqlDbContext _context;

    public ProductService(MySqlDbContext context)
    {
        _context = context;
    }

    public async Task<IEnumerable<Product>> GetAllAsync()
    {
        return await _context.Products.ToListAsync();
    }

    public async Task<Product?> GetByIdAsync(int id)
    {
        return await _context.Products.FindAsync(id);
    }

    public async Task<Product> CreateAsync(Product product)
    {
        _context.Products.Add(product);
        await _context.SaveChangesAsync();
        return product;
    }

    public async Task<Product> UpdateAsync(Product product)
    {
        _context.Products.Update(product);
        await _context.SaveChangesAsync();
        return product;
    }

    public async Task<bool> DeleteAsync(int id)
    {
        var product = await _context.Products.FindAsync(id);
        if (product == null)
            return false;

        _context.Products.Remove(product);
        await _context.SaveChangesAsync();
        return true;
    }
}
